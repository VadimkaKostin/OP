x = float(input('Enter x: '))   #Введення x
n = int(input('Enter n: '))     #Введення n

S = 0                           #Початкове значення S

for i in range(n):              #Початок арифметичного циклу
    a = i + 1                   #Заміна порядкового номеру на лічильник
    S += (-1)**(a-1) * x**a / a #Додавання до S наступного члену ряду

print('S = ',S)                 #Виведення S

input('Press ENTER to exit')    #Завершення програми
